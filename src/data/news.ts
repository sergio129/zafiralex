// Función para obtener una noticia por su slug
export function getNewsBySlug(slug: string): NewsItem | undefined {
  return newsData.find(item => item.slug === slug);
}

// Función para obtener todas las categorías
export function getAllCategories(): string[] {
  return ['Todas', ...Array.from(new Set(newsData.map(item => item.category)))];
}

// Función para filtrar noticias por categoría
export function getNewsByCategory(category: string): NewsItem[] {
  return category === 'Todas' ? newsData : newsData.filter(item => item.category === category);
}

// Función para obtener noticias destacadas
export function getFeaturedNews(): NewsItem[] {
  return newsData.filter(item => item.featured);
}
